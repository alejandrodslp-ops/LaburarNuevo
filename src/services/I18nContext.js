// ── Context ──────────────────────────────────────────────────────────────────
const I18nContext = createContext({
  idioma: 'es',
  setIdioma: () => {},
  t: (key) => key,
});

// Mapa de código de idioma del dispositivo → código de idioma de la app
const LOCALE_MAP = { de: 'de', pt: 'pt', en: 'en', fr: 'fr', it: 'it', es: 'es', sv: 'sv', no: 'no', ja: 'ja', hi: 'hi' };

function detectarIdiomaDispositivo() {
  try {
    const locale = Localization.getLocales?.()?.[0]?.languageCode
      ?? Localization.locale?.split('-')?.[0]
      ?? 'es';
    return LOCALE_MAP[locale] ?? 'es';
  } catch {
    return 'es';
  }
}

export function I18nProvider({ children }) {
  const [idioma, setIdiomaState] = useState('es');

  useEffect(() => {
    AsyncStorage.getItem(STORAGE_KEY).then(saved => {
      if (saved && TR[saved]) {
        setIdiomaState(saved);
      } else {
        // Primera vez: detectar idioma del dispositivo y guardarlo
        const detectado = detectarIdiomaDispositivo();
        setIdiomaState(detectado);
        AsyncStorage.setItem(STORAGE_KEY, detectado);
      }
    });
  }, []);

  const setIdioma = useCallback(async (lang) => {
    if (!TR[lang]) return;
    setIdiomaState(lang);
    await AsyncStorage.setItem(STORAGE_KEY, lang);
  }, []);

  const t = useCallback((key, params = {}) => {
    const str = TR[idioma]?.[key] ?? TR['es']?.[key] ?? key;
    return str.replace(/\{(\w+)\}/g, (_, k) =>
      params[k] !== undefined ? String(params[k]) : `{${k}}`
    );
  }, [idioma]);

  const value = useMemo(() => ({ idioma, setIdioma, t }), [idioma, setIdioma, t]);

  return (
    <I18nContext.Provider value={value}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}
